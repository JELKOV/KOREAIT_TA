# 문자열 길이 반환
len("Hello")  # 5

# Subscripting (문자열 인덱싱)
print("Hello"[0])   # H (첫 번째 문자)
print("Hello"[4])   # o (다섯 번째 문자)
print("Hello"[-1])  # o (마지막 문자, 음수 인덱싱)

# String (문자열 결합)
print("123" + "345")  # 123345 (문자열끼리 연결됨)

# Integer (정수형)
print(123 + 345)  # 468 (숫자 연산)

# 큰 숫자에 언더스코어(_) 사용 -> 가독성을 높임
print(123_456_789)  # 123456789 (출력 시 언더스코어는 무시됨)

# Float (실수형)
print(3.14159)  # 3.14159

# Boolean (불리언)
print(True)   # True
print(False)  # False
